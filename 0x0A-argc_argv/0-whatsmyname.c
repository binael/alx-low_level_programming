#include <stdio.h>

/**
 * main - print the name of the program
 * @argc: the number of command line inputs
 * @argv: the name of the command line string input
 *
 * Return: Always 0 (success)
 */

int main(int __attribute__((__unused__)) argc, char *argv[])
{
	printf("%s\n", argv[0]);

	return (0);
}
